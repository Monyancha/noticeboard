import UIKit

extension UIColor {
    
    class func usiuGold() -> UIColor {
        return UIColor(red: 1, green: 0.79, blue: 0.31, alpha: 1)
    }
    
    class func usiuBlue() -> UIColor {
        return UIColor(red: 1/255, green: 41/255, blue: 81/255, alpha: 1)
    }
    
}
